#include <iostream>

using namespace std;

int main()
{
    cout <<"Using for loop" << endl;

    for(int i=0; i<=5;i++)
    {
        cout <<"value of i: "<<i<<endl;
    }

    cout <<endl<<"Using while loop" << endl;

    int i=0;

    while (i<=5)
    {
        cout <<"value of i: "<<i<<endl;
        i++;
    }
    

}
